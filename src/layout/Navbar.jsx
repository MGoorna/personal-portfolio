import {   useEffect, useState } from "react"
import Button from "../components/Button"
import { Menu, X } from "lucide-react"

const navLinks = [
  {
    name: 'Home',
    href: '#home',
  },
  {
    name: 'About',
    href: '#about',
  },
  {
    name: 'Projects',
    href: '#projects',
  },
  {
    name: 'Testimonials',
    href: '#testimonials',
  },
  {
    name: 'Contact',
    href: '#contact',
  },
] 

const Navbar = () => {
  const [isMobileMenuOpen, setIsMobileMenuOpen] = useState(false)
  const [isSccrolled, setIsScrolled] = useState(false);

  useEffect(() => {
    const handleScroll = () => {
      setIsScrolled(window.scrollY > 50);
    };
    window.addEventListener('scroll', handleScroll);
    return () => {
      window.removeEventListener('scroll', handleScroll);
    };
  })

  return (
    <header className={`transition-all duration-300 ease-in-out ${isSccrolled ? 'glass-strong py-3' : 'bg-transparent py-5'} fixed top-0 left-0 right-0  z-50`}>
      <nav className='container mx-auto px-6 flex justify-between items-center'>
        <a href="#" className='text-xl font-bold tracking-tight hover:text-primary'> 
          MG <span className='text-primary'>.</span>
        </a>
        <div className='hidden md:flex item-center gap-1'> 
          <div className='glass rounded-full px-2 py-1 flex item-center gap-1'>
            {navLinks.map((link, index) => (
              <a 
                href={link.href} 
                key={index}
                className='px-4 py-2 text-sm text-muted-foreground hover:text-foreground rounded-full hover:bg-surface'
                >
                {link.name}
              </a>
            ))}
          </div>
        </div>
        <div className="hidden md:block">
          <Button size='sm'>Hire Me</Button>
        </div>
        <button 
          className="md:hidden p-2 text-foreground cursor-pointer" 
          onClick={() => setIsMobileMenuOpen(!isMobileMenuOpen)}>
          {isMobileMenuOpen ? <X size={24}/> : <Menu size={24}/>} 
        </button>
      </nav>

     {/*  Mobile menu */}
     {isMobileMenuOpen && <div className="md:hidden glass-strong animate-fade-in ">
      <div className="container mx-auto px-6 py-6 flex flex-col gap-4">
        {navLinks.map((link, index) => (
          <a 
            href={link.href}
            key={index}
            className='text-lg text-muted-foreground hover:text-foreground py-2'
            onClick={() => setIsMobileMenuOpen(!isMobileMenuOpen)}
          >
              {link.name}
          </a>
        ))}
        <Button onClick={() => setIsMobileMenuOpen(!isMobileMenuOpen)}>Contact Me</Button>
      </div>
     </div>
    }
    </header>
  )
}

export default Navbar
